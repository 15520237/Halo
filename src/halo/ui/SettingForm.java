            Setting.SetServerIP(txtServerIP.getText().trim());
        if(!txtServerPort.getText().trim().isEmpty())
            Setting.SetServerPort(Integer.parseInt(txtServerPort.getText().trim()));
        if(!jComboBoxClientIP.getSelectedItem().toString().trim().isEmpty())
            Setting.SetIP(jComboBoxClientIP.getSelectedItem().toString().trim());
        JOptionPane.showMessageDialog(null, "Saved successfully!", "Message!", JOptionPane.DEFAULT_OPTION);
