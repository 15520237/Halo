    public SettingForm() {
        initComponents();
        this.getContentPane().setBackground(Color.WHITE);
        for (int i = 0; i < jComboBoxClientIP.getComponentCount(); i++) {
            if (jComboBoxClientIP.getComponent(i) instanceof JComponent) {
                ((JComponent) jComboBoxClientIP.getComponent(i)).setBorder(new EmptyBorder(0, 0, 0, 0));
            }

            if (jComboBoxClientIP.getComponent(i) instanceof AbstractButton) {
                ((AbstractButton) jComboBoxClientIP.getComponent(i)).setBorderPainted(false);
            }
        }

        //add data
        txtServerIP.setText(Setting.GetServerIP());
        txtServerPort.setText(String.valueOf(Setting.GetServerPort()));

        try {
            ArrayList<InetAddress> clientIPs = Setting.GetIPs();
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            for (int i = 0; i < clientIPs.size(); i++) {
                model.addElement(clientIPs.get(i).getHostAddress());
            }
            jComboBoxClientIP.setModel(model);
            InetAddress myIP = Setting.GetIP();
            if (myIP != null) {
                jComboBoxClientIP.setSelectedItem(myIP.getHostAddress());
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex, "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }

